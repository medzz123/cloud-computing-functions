import { Response } from 'express';

import admin from '../db';
import { CustomRequest } from '../types/request';
import { tinyid } from '../utils/uid';

export interface CreateUserBody {
  email: string;
  password: string;
  name: string;
}

export const createUser = async (
  req: CustomRequest<CreateUserBody>,
  res: Response
) => {
  try {
    const { email, password, name } = req.body;

    if (!email || !password || !name) {
      return res.status(401).send('Not all details have been provided');
    }

    const autoGeneratedUsername = `${name
      .toLowerCase()
      .trim()
      .split(' ')
      .join('-')}-${tinyid()}`;

    const user = await admin.auth().createUser({
      email,
      password,
      displayName: autoGeneratedUsername,
    });

    return res.send({
      username: autoGeneratedUsername,
      email: user.email,
      name,
    });
  } catch (err) {
    console.log('Failed on creating a user', err);
    return res.status(500).send('Internal Server Error');
  }
};
